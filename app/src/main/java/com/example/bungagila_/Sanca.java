package com.example.bungagila_;

public class Sanca {
    private String namaSanca;
    private String detailSanca;
    private int gambarSanca;

    public String getNamaSanca() {
        return namaSanca;
    }

    public void setNamaSanca(String namaSanca) {
        this.namaSanca = namaSanca;
    }

    public String getDetailSanca() {
        return detailSanca;
    }

    public void setDetailSanca(String detailSanca) {
        this.detailSanca = detailSanca;
    }

    public int getGambarSanca() {
        return gambarSanca;
    }

    public void setGambarSanca(int gambarSanca) {
        this.gambarSanca = gambarSanca;
    }
}
